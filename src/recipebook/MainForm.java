    public void showRecpeDetails(Recipe r){
        Path resourceDirectory = Paths.get("src","resources");
        String absolutePath = resourceDirectory.toFile().getAbsolutePath();
        ImageIcon imgIcon = new ImageIcon(absolutePath+"/"+r.getImageUrl());
        
        jIconRecipeImage.setIcon(imgIcon);
        jLblRecipeTitle.setText(r.getTitle());
        jLblRecipeInstructions.setText(r.getInstructions());
        jLblRecipeIngridients.setText(r.getIngredients());
        
    }
    
