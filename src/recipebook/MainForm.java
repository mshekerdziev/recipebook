    public void loadRecipes(){
        int x=0,y=0;
        for(Recipe r : recipes){
            RecipeDetails rd =  new RecipeDetails(this,r);
            rd.setBounds(x, y, 500, 110);
            y+=100;
            jPanelList.add(rd);
        }
    }
